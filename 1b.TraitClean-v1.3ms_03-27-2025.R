# Assume the following objects are defined:
# M1: matrix1 (log2(abundance)) with dimensions [n_assays x m1_samples]
# M2: matrix2 (log2(abundance)) with dimensions [n_assays x m2_samples]
# commonRows: vector of row indices (assays) to use (e.g. a set of >5000 rows known to be preserved)

# Step 1: Subset the matrices to the common assays.
M1_sub <- M1[commonRows, ]
M2_sub <- M2[commonRows, ]

# Step 2: Standardize each assay (row) to remove differences in overall normalization.
# This function computes z-scores across samples for each assay.
zscoreRows <- function(mat) {
  t(apply(mat, 1, function(x) {
    if (sd(x, na.rm=TRUE) > 0) {
      (x - mean(x, na.rm=TRUE)) / sd(x, na.rm=TRUE)
    } else {
      rep(0, length(x))
    }
  }))
}

M1_z <- zscoreRows(M1_sub)
M2_z <- zscoreRows(M2_sub)

# Step 3: Compute the correlation matrix between columns (samples) of M1 and M2.
# This yields a matrix of dimensions [m1_samples x m2_samples]
corrMat <- cor(M1_z, M2_z, use = "pairwise.complete.obs")

# Step 4: Create a cost matrix by taking the negative of the correlations.
# (We want to maximize correlation, so we minimize negative correlation.)
costMat <- -corrMat

# Step 5: Solve the assignment problem.
# Matrix1 has fewer samples than matrix2 (m1 < m2). We pad costMat to make it square.
m1 <- nrow(costMat)
m2 <- ncol(costMat)
if(m1 < m2){
  # Pad extra rows with a high cost so they are never selected.
  pad <- matrix(max(costMat, na.rm=TRUE) + 1, nrow = m2 - m1, ncol = m2)
  costMatPad <- rbind(costMat, pad)
} else {
  costMatPad <- costMat
}

# Solve the assignment problem using Hungarian algorithm implemented in C++, rather than lp.assign (slow linear programming):
assignment_sol <- clue::solve_LSAP(costMatPad+if(min(costMatPad)<0) { abs(min(costMatPad)) } else { 0 })  # slower:  #  lpSolve::lp.assign(costMatPad)$solution

# If lpSolve was used:
## Extract the solution for the real rows (matrix1 columns).
#assignment_mat <- assignment_sol[1:m1, , drop=FALSE]
#
## For each column of matrix1 (each row in the cost matrix), get the matched column in M2.
#matchIndex <- apply(assignment_mat, 1, function(x) which(x == 1))
#
## Step 6: Order the indices of matrix1 in the increasing order of the matched M2 sample indices.
## (This vector indicates the order of M1 columns most consistent with the M2 ordering.)
#sorted_M1_indices <- order(matchIndex)
#
## Output the result:
#sorted_M1_indices  # This vector gives the ordering of matrix1's columns.

## Output M2 ordered according to the optimal solution from clue::solve_LSAP(), and QC using bicor of all common rows.
M2.ordered_sub<-M2[commonRows, as.vector(assignment_sol)[1:ncol(M1)] ]

library(WGCNA)
commonRowBicors<-sapply(rownames(M1_sub),function(x) bicor(M1_sub[x,],M2.ordered_sub[x,], use='p'))
