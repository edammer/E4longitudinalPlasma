#  Concatenate disease annotations in a new data frame column "numericMeta.CSF$Group", checking separate columns of the data frame numericMeta.CSF when more than one
# of the following columns have a value of "AD", "MCI", "FTD", "PD", and/or "ALS", and none of the columns have a value of "CT" in that row for any of these named columns of numericMeta.CSF:
# "Group.AD.CT", "Group.MCI.CT", "Group.FTD.CT", "Group.PD.CT", and "Group.ALS.CT".  Further, if only one disease annotation is found, set the Group column value for that row to the one disease annotation.
# Finally, if only "CT" is found in the checked columns, set the Group column value for that row to "CT".
cols <- c("Group.AD.CT", "Group.MCI.CT", "Group.FTD.CT", "Group.PD.CT", "Group.ALS.CT")  #  vector of colnames to check
numericMeta.CSF$Group <- apply(numericMeta.CSF[, cols], 1, function(x) {
  # Remove NA values
  x <- na.omit(x)

  # If no values, return NA
  if (length(x) == 0) return(NA)

  # If all values are "CT", then set Group to "CT"
  if (all(x == "CT")) return("CT")

  # Otherwise, remove any "CT" values and use the remaining disease annotations
  #x <- x[x != "CT"]

  # Get the unique annotations (should be one or more of: "AD", "MCI", "FTD", "PD", "ALS")
  x <- unique(x)

  # If there's only one annotation, return it; if more than one, concatenate them.
  if (length(x) == 1) {
    return(x)
  } else {
    return(paste(x, collapse = "."))
  }
})
